# Function to greet a list of names
def greet_names(names):
    # Loop through the list of names
    for name in names:
        print(f"Hello, {name}!")  # Using an f-string for formatting


# List of names (variable)
name_list = ["Alice", "Bob", "Charlie"]

# Calling the function with the variable
greet_names(name_list)
